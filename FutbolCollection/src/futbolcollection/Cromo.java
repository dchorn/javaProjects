
package futbolcollection;

public class Cromo {
    String nombre;
    String apellido;
    int numero;
    Equipo equipo;
    
    public Cromo(String nombre, String apellido, int numero, Equipo equipo) {
    	this.nombre = nombre;
    	this.apellido = apellido;
    	this.numero = numero;
    	this.equipo = equipo;
    }
}